    private boolean checkInput(String maVe, String maDK, String tenTour, String maLT, String giaVe, String ghiChu) {
        setlblErrorEmpty();

        if(maVe.isEmpty())
        {
            lblMaVeError.setText("Mã vé không được để trống");
            return false;
        }
        else if(!maVe.matches("^\\d{3}$"))
        {
            lblMaVeError.setText("Mã vé sai định dạng");
            return false;
        }
        else if(maDK.isEmpty())
        {
            lblMaDuKhachError.setText("Mã du khách không được để trống");
            return false;
        }
        else if(!maDK.matches("^(DK)\\d{2}$"))
        {
            lblMaDuKhachError.setText("Mã du khách sai định dạng");
            return false;
        }
        else if(tenTour.isEmpty())
        {
            lblTenTourError.setText("Tên tour không được để trống");
            return false;
        }
        else if(maLT.isEmpty())
        {
            lblMaLoTrinhError.setText("Mã lô trình không được để trống");
            return false;
        }
        else if(!maLT.matches("^(LT)\\d{2}$"))
        {
            lblMaLoTrinhError.setText("Mã lô trình sai định dạng");
            return false;
        }   
        else if(giaVe.isEmpty())
        {
            lblGiaVeError.setText("Tên tour không được để trống");
            return false;
        }
        else if(TableVe.dsVe.indexOf(maVe) >= 0)
        {
            lblMaVe.setText("Mã vé đã tồn tại");
            return false;
        }

        return true;
    }

