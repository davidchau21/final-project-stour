    private void sapXepDSTour(int type) {
        List<String> dsMaTour = new ArrayList<String>();
        
        for(int i = 0; i < tbTour.getRowCount(); i++)
            dsMaTour.add((String)tbTour.getValueAt(i, 0));
        
        List<Tour> dsTour = new ArrayList<Tour>();
        for (String ma : dsMaTour) {
            Tour t = tableTour.getTour(ma);
            if(t != null)
                dsTour.add(t);
        }
        
        if(type == 0)
        {
            loadData();
        }
        if(type == 1)
        {
            for(int i = 0; i < dsTour.size() - 1; i++){
                for(int j = i + 1; j < dsTour.size(); j++)
                {
                    if(dsTour.get(i).getTenTour().compareToIgnoreCase(dsTour.get(j).getTenTour()) > 0)
                    {
                        Tour temp = dsTour.get(i);
                        dsTour.set(i, dsTour.get(j));
                        dsTour.set(j, temp);
                    }
                }
            }
            tableTour.addDataToTable(dsTour);
        }
        if(type == 2)
        {
            for(int i = 0; i < dsTour.size() - 1; i++){
                for(int j = i + 1; j < dsTour.size(); j++)
                {
                    if(dsTour.get(i).getTenTour().compareToIgnoreCase(dsTour.get(j).getTenTour()) < 0)
                    {
                        Tour temp = dsTour.get(i);
                        dsTour.set(i, dsTour.get(j));
                        dsTour.set(j, temp);
                    }
                }
            }
            tableTour.addDataToTable(dsTour);
        }
        if(type == 3)
        {
            for(int i = 0; i < dsTour.size() - 1; i++){
                for(int j = i + 1; j < dsTour.size(); j++)
                {
                    if(Double.valueOf(dsTour.get(i).getGiaTour()) > Double.valueOf(dsTour.get(j).getGiaTour()))
                    {
                        Tour temp = dsTour.get(i);
                        dsTour.set(i, dsTour.get(j));
                        dsTour.set(j, temp);
                    }
                }
            }
            tableTour.addDataToTable(dsTour);
        }
        if(type==4)
        {
            for(int i = 0; i < dsTour.size() - 1; i++){
                for(int j = i + 1; j < dsTour.size(); j++)
                {
                    if(Double.valueOf(dsTour.get(i).getGiaTour()) < Double.valueOf(dsTour.get(j).getGiaTour()))
                    {
                        Tour temp = dsTour.get(i);
                        dsTour.set(i, dsTour.get(j));
                        dsTour.set(j, temp);
                    }
                }
            }
            tableTour.addDataToTable(dsTour);
        }
        
    }

