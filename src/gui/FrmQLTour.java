    private void btnCapNhatMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCapNhatMousePressed
        // TODO add your handling code here:
        if(btnCapNhat.getText().compareToIgnoreCase("LƯU") == 0)
        {
            String ma = txtMaTour.getText().trim();
            String ten = txtTenTour.getText().trim();
            float gia = Float.parseFloat(txtGiaTour.getText().trim());
            
//            String strVaiTro = (String)cbxVaiTro.getSelectedItem();
//            boolean vaiTro = true;
//            if(strVaiTro.compareToIgnoreCase("Quản lý") == 0)
//                vaiTro = false;
            
            int soNgay = Integer.parseInt(txtSoNgay.getText().trim());
            String ghiChu = txtGhiChu.getText().trim();
            
            if(validTour(ma, ten, gia, soNgay))
            {
                Tour t = new Tour(ma, ten, soNgay, gia, ghiChu);
                if (Tour_DAO.updateTour(t)) 
                {
                    loadData();
                    JOptionPane.showMessageDialog(null, "Cập nhật thông tin tour thành công");
                    btnCapNhat.setText("CẬP NHẬT");
                    btnHuy.setVisible(false);
                    setTextFieldEditable(false);
                } 
                else
                    JOptionPane.showMessageDialog(null, "Cập nhật thông tin tour không thành công");
            }
        }
        else
        {
            btnCapNhat.setText("LƯU");
            btnHuy.setVisible(true);
            setTextFieldEditable(true);
        }
    }//GEN-LAST:event_btnCapNhatMousePressed

